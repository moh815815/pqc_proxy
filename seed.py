def seed_default_tenant():
    """إنشاء مستأجر افتراضي للاختبار"""
    print("⟹  إنشاء المستأجر الافتراضي...")

    if Tenant.query.filter_by(النطاق_الرئيسي="pqc-demo.localhost").first():
        print("    → المستأجر الافتراضي موجود مسبقاً\n")
        return

    مستأجر = Tenant(
        اسم_المستأجر="مؤسسة العرض التجريبي",
        النطاق_الرئيسي="pqc-demo.localhost",
        خطة_الاشتراك="احترافي",
        مستوى_أمان_NIST=3,
        الحد_الأقصى_للشهادات=100,
        بيانات_تعريف={"وصف": "بيئة اختبار وتطوير النظام"},
    )
    db.session.add(مستأجر)
    db.session.flush()

    # مستخدم مدير افتراضي
    مدير = User(
        معرف_المستأجر=مستأجر.معرف,
        البريد_الإلكتروني="admin@pqc-demo.localhost",
        كلمة_المرور_مشفرة=generate_password_hash("Admin@PQC2024!"),
        الاسم_الكامل="مدير النظام التجريبي",
        الدور="مدير",
    )
    db.session.add(مدير)

    # سياسة ترحيل افتراضية
    سياسة = MigrationPolicy(
        معرف_المستأجر=مستأجر.معرف,
        اسم_السياسة="سياسة الترقية التلقائية إلى ML-KEM-768",
        الخوارزمية_المصدر="ML-KEM-512",
        الخوارزمية_الهدف="ML-KEM-768",
        الترحيل_التلقائي=True,
        أيام_التحذير_قبل_الانتهاء=30,
        شروط_الترحيل={"السبب": "ترقية دورية للمستوى 3"},
    )
    db.session.add(سياسة)
    db.session.commit()

    print(f"    ✓ مستأجر: {مستأجر.اسم_المستأجر} (معرف: {مستأجر.معرف})")
    print(f"    ✓ مدير:   admin@pqc-demo.localhost  |  كلمة المرور: Admin@PQC2024!")
    print(f"    ✓ سياسة ترحيل افتراضية\n")
    return مستأجر


