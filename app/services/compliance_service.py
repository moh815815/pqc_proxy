"""
خدمة سجل الامتثال - Compliance Ledger Service
سجل تدقيق غير قابل للتعديل بالمصطلحات التقنية العربية
"""

from datetime import datetime, timezone, timedelta
from app import db
import hashlib


class ComplianceService:
    """خدمة سجل الامتثال الأمني للتشفير ما بعد الكمومي"""

    def سجّل_حدث(self, نوع_الحدث: str, وصف: str,
                   معرف_المستأجر: str = None,
                   معرف_الكيان: str = None, نوع_الكيان: str = None,
                   خطورة: str = "معلومات", الخوارزمية: str = None,
                   التفاصيل: dict = None, معيار: str = None,
                   عنوان_IP: str = None, معرف_مستخدم: str = None) -> None:
        """تسجيل حدث في سجل الامتثال مع بصمة التكامل"""
        from app.models import ComplianceLedger

        now = datetime.now(timezone.utc)
        # بصمة للتحقق من عدم التلاعب
        محتوى_البصمة = f"{نوع_الحدث}:{وصف}:{now.isoformat()}:{معرف_المستأجر}"
        بصمة = hashlib.sha256(محتوى_البصمة.encode()).hexdigest()

        سجل = ComplianceLedger(
            معرف_المستأجر=معرف_المستأجر,
            نوع_الحدث=نوع_الحدث,
            الكيان_المرجعي=معرف_الكيان,
            نوع_الكيان=نوع_الكيان,
            خطورة_الحدث=خطورة,
            وصف_الحدث=وصف,
            تفاصيل_تقنية=التفاصيل or {},
            الخوارزمية_المستخدمة=الخوارزمية,
            عنوان_IP_المصدر=عنوان_IP,
            معرف_المستخدم=معرف_مستخدم,
            بصمة_السجل=بصمة,
            معيار_الامتثال=معيار or "NIST SP 800-208",
        )
        db.session.add(سجل)
        # لا نودع هنا - يتم الإيداع من الدالة المستدعية

    def توليد_تقرير_الامتثال(self, معرف_المستأجر: str,
                               من_تاريخ: datetime = None,
                               إلى_تاريخ: datetime = None) -> dict:
        """
        توليد تقرير الامتثال الشامل بالمصطلحات التقنية العربية
        Generate full Arabic compliance report
        """
        from app.models import ComplianceLedger, Tenant, Certificate, HandshakeSession, CertificateStatus, HandshakeState

        now = datetime.now(timezone.utc)
        من_تاريخ  = من_تاريخ  or (now - timedelta(days=30))
        إلى_تاريخ = إلى_تاريخ or now

        مستأجر = Tenant.query.get(معرف_المستأجر)
        if not مستأجر:
            return {"خطأ": "المستأجر غير موجود"}

        # ── جلب السجلات ────────────────────────────
        سجلات = ComplianceLedger.query.filter(
            ComplianceLedger.معرف_المستأجر == معرف_المستأجر,
            ComplianceLedger.طابع_زمني >= من_تاريخ,
            ComplianceLedger.طابع_زمني <= إلى_تاريخ,
        ).order_by(ComplianceLedger.طابع_زمني.desc()).all()

        # ── إحصاءات الشهادات ───────────────────────
        شهادات_نشطة     = Certificate.query.filter_by(معرف_المستأجر=معرف_المستأجر, الحالة=CertificateStatus.نشطة.value).count()
        شهادات_ملغاة    = Certificate.query.filter_by(معرف_المستأجر=معرف_المستأجر, الحالة=CertificateStatus.ملغاة.value).count()
        شهادات_مرحّلة   = Certificate.query.filter_by(معرف_المستأجر=معرف_المستأجر, الحالة=CertificateStatus.مرحّلة.value).count()

        # ── إحصاءات المصافحة ───────────────────────
        مصافحات_ناجحة   = HandshakeSession.query.filter_by(معرف_المستأجر=معرف_المستأجر, الحالة_الحالية=HandshakeState.مكتملة.value).count()
        مصافحات_فاشلة   = HandshakeSession.query.filter_by(معرف_المستأجر=معرف_المستأجر, الحالة_الحالية=HandshakeState.فاشلة.value).count()

        # ── تصنيف السجلات حسب الخطورة ──────────────
        def عد_بخطورة(مستوى): return sum(1 for س in سجلات if س.خطورة_الحدث == مستوى)

        # ── توليد التقرير ─────────────────────────
        تقرير = {
            "معلومات_التقرير": {
                "عنوان": "تقرير الامتثال لمعايير التشفير ما بعد الكمومي",
                "رقم_الوثيقة": f"PQC-COMP-{now.strftime('%Y%m%d%H%M')}",
                "معيار_المرجعي": "NIST SP 800-208 / ISO/IEC 27001",
                "المنظمة": مستأجر.اسم_المستأجر,
                "النطاق": مستأجر.النطاق_الرئيسي,
                "فترة_التقرير_من": من_تاريخ.strftime("%Y-%m-%d %H:%M UTC"),
                "فترة_التقرير_إلى": إلى_تاريخ.strftime("%Y-%m-%d %H:%M UTC"),
                "تاريخ_الإصدار": now.strftime("%Y-%m-%d %H:%M:%S UTC"),
                "مستوى_أمان_NIST_المطبق": مستأجر.مستوى_أمان_NIST,
                "درجة_الامتثال_الإجمالية": self._احسب_درجة_الامتثال(
                    مصافحات_ناجحة, مصافحات_فاشلة, شهادات_نشطة, شهادات_ملغاة
                ),
            },
            "ملخص_تنفيذي": {
                "عدد_الأحداث_الإجمالي": len(سجلات),
                "أحداث_معلوماتية": عد_بخطورة("معلومات"),
                "أحداث_تحذيرية":   عد_بخطورة("تحذير"),
                "أحداث_حرجة":       عد_بخطورة("حرج"),
            },
            "وضع_الشهادات_الرقمية": {
                "الشهادات_النشطة":      شهادات_نشطة,
                "الشهادات_الملغاة":     شهادات_ملغاة,
                "الشهادات_المرحّلة":    شهادات_مرحّلة,
                "إجمالي_الشهادات":     شهادات_نشطة + شهادات_ملغاة + شهادات_مرحّلة,
            },
            "إحصاءات_المصافحة_الكمومية": {
                "إجمالي_المصافحات":        مصافحات_ناجحة + مصافحات_فاشلة,
                "المصافحات_الناجحة":       مصافحات_ناجحة,
                "المصافحات_الفاشلة":       مصافحات_فاشلة,
                "معدل_نجاح_المصافحة":     f"{round(مصافحات_ناجحة / max(مصافحات_ناجحة + مصافحات_فاشلة, 1) * 100, 2)}%",
            },
            "سجل_الأحداث_التفصيلي": [
                {
                    "الرقم_التسلسلي": i + 1,
                    "الطابع_الزمني": س.طابع_زمني.strftime("%Y-%m-%d %H:%M:%S UTC"),
                    "نوع_الحدث": س.نوع_الحدث,
                    "مستوى_الخطورة": س.خطورة_الحدث,
                    "الوصف": س.وصف_الحدث,
                    "الخوارزمية": س.الخوارزمية_المستخدمة,
                    "معيار_الامتثال": س.معيار_الامتثال,
                    "بصمة_التكامل": س.بصمة_السجل[:16] + "..." if س.بصمة_السجل else None,
                }
                for i, س in enumerate(سجلات)
            ],
            "تقييم_الامتثال": self._توليد_تقييم_الامتثال(
                مستأجر, شهادات_نشطة, مصافحات_ناجحة, مصافحات_فاشلة, سجلات
            ),
            "التوصيات_الأمنية": self._توليد_توصيات(
                مستأجر, شهادات_نشطة, مصافحات_فاشلة
            ),
        }

        return تقرير

    @staticmethod
    def _احسب_درجة_الامتثال(ناجحة, فاشلة, نشطة, ملغاة) -> str:
        """حساب درجة الامتثال الإجمالية"""
        إجمالي_مصافحة = ناجحة + فاشلة
        معدل_نجاح = (ناجحة / max(إجمالي_مصافحة, 1)) * 100

        if معدل_نجاح >= 99 and نشطة > 0:   return "ممتاز - AA+"
        if معدل_نجاح >= 95:                  return "جيد جداً - AA"
        if معدل_نجاح >= 90:                  return "جيد - A"
        if معدل_نجاح >= 80:                  return "مقبول - B+"
        return "يحتاج مراجعة - B"

    @staticmethod
    def _توليد_تقييم_الامتثال(مستأجر, نشطة, ناجحة, فاشلة, سجلات) -> dict:
        """تقييم مفصل لمستوى الامتثال"""
        return {
            "الامتثال_لـ_NIST_SP_800-208":  "✓ مطبّق" if مستأجر.مستوى_أمان_NIST >= 3 else "✗ يحتاج تحديث",
            "الامتثال_لـ_ISO_IEC_27001":     "✓ مطبّق" if نشطة > 0 else "✗ لا توجد شهادات نشطة",
            "صحة_مسار_تدقيق_الأحداث":       "✓ سليم" if len(سجلات) > 0 else "تحذير: لا توجد سجلات",
            "نسبة_نجاح_المصافحة":            f"{round(ناجحة / max(ناجحة + فاشلة, 1) * 100, 1)}%",
            "الخوارزميات_المعتمدة":          "ML-KEM, ML-DSA, SLH-DSA (NIST 2024)",
            "حالة_الامتثال_الإجمالية":       "ممتثل" if مستأجر.مستوى_أمان_NIST >= 3 and نشطة > 0 else "يحتاج اتخاذ إجراء",
        }

    @staticmethod
    def _توليد_توصيات(مستأجر, نشطة, فاشلة) -> list:
        """توليد توصيات أمنية ديناميكية"""
        توصيات = []

        if مستأجر.مستوى_أمان_NIST < 3:
            توصيات.append({
                "الأولوية": "عالية",
                "التوصية": "رفع مستوى أمان NIST إلى المستوى 3 أو أعلى",
                "المعيار": "NIST SP 800-208 Section 4.2",
            })
        if نشطة == 0:
            توصيات.append({
                "الأولوية": "حرجة",
                "التوصية": "إصدار شهادات رقمية نشطة فوراً",
                "المعيار": "NIST SP 800-57 Part 1",
            })
        if فاشلة > 10:
            توصيات.append({
                "الأولوية": "متوسطة",
                "التوصية": "مراجعة أسباب فشل المصافحة وتحسين إعدادات TLS",
                "المعيار": "NIST SP 800-52r2",
            })
        توصيات.append({
            "الأولوية": "دورية",
            "التوصية": "إجراء تدقيق ربع سنوي لجميع الشهادات والخوارزميات المستخدمة",
            "المعيار": "ISO/IEC 27001:2022 Control A.8.24",
        })

        return توصيات
