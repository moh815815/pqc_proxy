"""
خدمة إدارة وترحيل الشهادات الرقمية
Certificate Lifecycle & Migration Service
ترحيل ديناميكي متعدد المستأجرين للشهادات المقاومة للكم
"""

from datetime import datetime, timezone, timedelta
from app import db
from app.models import (Certificate, CertificateStatus, PQCAlgorithm,
                         MigrationPolicy, Tenant)
from app.services.compliance_service import ComplianceService
import hashlib
import secrets
import uuid


class CertificateService:
    """خدمة دورة حياة الشهادات الرقمية الكمومية"""

    compliance = ComplianceService()

    # ──────────────────────────────────────────────
    # إصدار شهادة جديدة
    # ──────────────────────────────────────────────
    @classmethod
    def إصدار_شهادة(cls, معرف_المستأجر: str, معرف_الخوارزمية: str,
                      الاسم_الشائع: str, المنظمة: str = None,
                      الدولة: str = "SA", أيام_الصلاحية: int = 365,
                      شهادة_جذر: bool = False) -> dict:
        """إصدار شهادة رقمية جديدة مقاومة للحواسيب الكمومية"""

        مستأجر   = Tenant.query.get(معرف_المستأجر)
        خوارزمية = PQCAlgorithm.query.get(معرف_الخوارزمية)

        if not مستأجر:
            return {"نجح": False, "خطأ": "المستأجر غير موجود"}
        if not خوارزمية or not خوارزمية.مفعّل:
            return {"نجح": False, "خطأ": "الخوارزمية غير متاحة"}

        # التحقق من حد الشهادات
        عدد_الشهادات_الحالية = Certificate.query.filter_by(
            معرف_المستأجر=معرف_المستأجر,
            الحالة=CertificateStatus.نشطة.value
        ).count()

        if عدد_الشهادات_الحالية >= مستأجر.الحد_الأقصى_للشهادات:
            return {
                "نجح": False,
                "خطأ": f"تم الوصول للحد الأقصى: {مستأجر.الحد_الأقصى_للشهادات} شهادة"
            }

        # توليد مفتاح عام مزيف (في الإنتاج: استخدم liboqs أو OpenSSL PQC)
        مفتاح_عام = cls._توليد_مفتاح_عام(خوارزمية)
        بصمة      = cls._حساب_بصمة(مفتاح_عام, الاسم_الشائع, معرف_المستأجر)

        now = datetime.now(timezone.utc)
        شهادة = Certificate(
            معرف_المستأجر=معرف_المستأجر,
            معرف_الخوارزمية=معرف_الخوارزمية,
            الاسم_الشائع=الاسم_الشائع,
            المنظمة=المنظمة or مستأجر.اسم_المستأجر,
            الدولة=الدولة,
            بصمة_الشهادة=بصمة,
            المفتاح_العام=مفتاح_عام[:500],  # مختصر
            تاريخ_الانتهاء=now + timedelta(days=أيام_الصلاحية),
            شهادة_الجذر=شهادة_جذر,
            بيانات_X509=cls._توليد_X509(الاسم_الشائع, المنظمة, الدولة, خوارزمية.اسم_الخوارزمية),
        )

        db.session.add(شهادة)
        db.session.flush()  # للحصول على المعرف قبل الإيداع

        cls.compliance.سجّل_حدث(
            نوع_الحدث="إصدار_شهادة",
            معرف_المستأجر=معرف_المستأجر,
            معرف_الكيان=شهادة.معرف,
            نوع_الكيان="شهادة",
            الوصف=f"إصدار شهادة رقمية جديدة: {الاسم_الشائع} باستخدام {خوارزمية.اسم_الخوارزمية}",
            الخوارزمية=خوارزمية.اسم_الخوارزمية,
            معيار="NIST SP 800-208",
        )

        db.session.commit()
        return {"نجح": True, "الشهادة": شهادة.to_dict()}

    # ──────────────────────────────────────────────
    # ترحيل شهادة إلى خوارزمية جديدة
    # ──────────────────────────────────────────────
    @classmethod
    def ترحيل_شهادة(cls, معرف_الشهادة: str, معرف_الخوارزمية_الجديدة: str,
                      سبب: str = "ترقية أمنية دورية") -> dict:
        """
        ترحيل شهادة من خوارزمية قديمة إلى أحدث خوارزمية PQC
        Dynamic Certificate Migration with Full Audit Trail
        """
        شهادة_قديمة = Certificate.query.get(معرف_الشهادة)
        if not شهادة_قديمة:
            return {"نجح": False, "خطأ": "الشهادة غير موجودة"}

        if شهادة_قديمة.الحالة not in [CertificateStatus.نشطة.value, CertificateStatus.قيد_الترحيل.value]:
            return {"نجح": False, "خطأ": f"لا يمكن ترحيل شهادة في الحالة: {شهادة_قديمة.الحالة}"}

        خوارزمية_جديدة = PQCAlgorithm.query.get(معرف_الخوارزمية_الجديدة)
        if not خوارزمية_جديدة:
            return {"نجح": False, "خطأ": "الخوارزمية الجديدة غير موجودة"}

        # وضع الشهادة القديمة في حالة "قيد الترحيل"
        شهادة_قديمة.الحالة = CertificateStatus.قيد_الترحيل.value
        شهادة_قديمة.سبب_الترحيل = سبب
        db.session.flush()

        # إصدار شهادة جديدة مرتبطة بالقديمة
        مفتاح_عام_جديد = cls._توليد_مفتاح_عام(خوارزمية_جديدة)
        بصمة_جديدة     = cls._حساب_بصمة(
            مفتاح_عام_جديد,
            شهادة_قديمة.الاسم_الشائع,
            شهادة_قديمة.معرف_المستأجر
        )

        # احتساب الصلاحية المتبقية من الشهادة القديمة
        now = datetime.now(timezone.utc)
        صلاحية_متبقية = (شهادة_قديمة.تاريخ_الانتهاء - now).days if شهادة_قديمة.تاريخ_الانتهاء > now else 365

        شهادة_جديدة = Certificate(
            معرف_المستأجر=شهادة_قديمة.معرف_المستأجر,
            معرف_الخوارزمية=معرف_الخوارزمية_الجديدة,
            الاسم_الشائع=شهادة_قديمة.الاسم_الشائع,
            المنظمة=شهادة_قديمة.المنظمة,
            الدولة=شهادة_قديمة.الدولة,
            بصمة_الشهادة=بصمة_جديدة,
            المفتاح_العام=مفتاح_عام_جديد[:500],
            تاريخ_الانتهاء=now + timedelta(days=max(صلاحية_متبقية, 90)),
            معرف_الشهادة_السابقة=شهادة_قديمة.معرف,
            سبب_الترحيل=سبب,
            بيانات_X509=cls._توليد_X509(
                شهادة_قديمة.الاسم_الشائع,
                شهادة_قديمة.المنظمة,
                شهادة_قديمة.الدولة,
                خوارزمية_جديدة.اسم_الخوارزمية
            ),
        )
        db.session.add(شهادة_جديدة)

        # إكمال الترحيل: أرشفة القديمة
        شهادة_قديمة.الحالة = CertificateStatus.مرحّلة.value
        db.session.flush()

        cls.compliance.سجّل_حدث(
            نوع_الحدث="ترحيل_شهادة",
            معرف_المستأجر=شهادة_قديمة.معرف_المستأجر,
            معرف_الكيان=شهادة_جديدة.معرف,
            نوع_الكيان="شهادة",
            الوصف=(
                f"ترحيل الشهادة '{شهادة_قديمة.الاسم_الشائع}' "
                f"من {شهادة_قديمة.الخوارزمية.اسم_الخوارزمية if شهادة_قديمة.الخوارزمية else 'غير محدد'} "
                f"إلى {خوارزمية_جديدة.اسم_الخوارزمية}. السبب: {سبب}"
            ),
            الخوارزمية=خوارزمية_جديدة.اسم_الخوارزمية,
            التفاصيل={
                "معرف_الشهادة_القديمة": شهادة_قديمة.معرف,
                "معرف_الشهادة_الجديدة": شهادة_جديدة.معرف,
                "الخوارزمية_القديمة": شهادة_قديمة.الخوارزمية.اسم_الخوارزمية if شهادة_قديمة.الخوارزمية else None,
                "الخوارزمية_الجديدة": خوارزمية_جديدة.اسم_الخوارزمية,
            },
            معيار="NIST SP 800-208",
        )

        db.session.commit()
        return {
            "نجح": True,
            "رسالة": "تم الترحيل بنجاح",
            "الشهادة_الجديدة": شهادة_جديدة.to_dict(),
            "الشهادة_القديمة_المعرف": شهادة_قديمة.معرف,
        }

    # ──────────────────────────────────────────────
    # إلغاء شهادة
    # ──────────────────────────────────────────────
    @classmethod
    def إلغاء_شهادة(cls, معرف_الشهادة: str, سبب: str) -> dict:
        """إلغاء شهادة رقمية مع تسجيل أمني كامل"""
        شهادة = Certificate.query.get(معرف_الشهادة)
        if not شهادة:
            return {"نجح": False, "خطأ": "الشهادة غير موجودة"}

        if شهادة.الحالة == CertificateStatus.ملغاة.value:
            return {"نجح": False, "خطأ": "الشهادة ملغاة مسبقاً"}

        شهادة.الحالة = CertificateStatus.ملغاة.value
        شهادة.تاريخ_الإلغاء = datetime.now(timezone.utc)
        شهادة.سبب_الإلغاء = سبب

        cls.compliance.سجّل_حدث(
            نوع_الحدث="إلغاء_شهادة",
            معرف_المستأجر=شهادة.معرف_المستأجر,
            معرف_الكيان=شهادة.معرف,
            نوع_الكيان="شهادة",
            الوصف=f"إلغاء الشهادة '{شهادة.الاسم_الشائع}'. السبب: {سبب}",
            خطورة="تحذير",
            الخوارزمية=شهادة.الخوارزمية.اسم_الخوارزمية if شهادة.الخوارزمية else None,
        )

        db.session.commit()
        return {"نجح": True, "رسالة": "تم الإلغاء بنجاح"}

    # ──────────────────────────────────────────────
    # الشهادات المنتهية قريباً
    # ──────────────────────────────────────────────
    @staticmethod
    def الشهادات_قرب_الانتهاء(معرف_المستأجر: str, أيام: int = 30) -> list:
        """قائمة الشهادات التي ستنتهي خلال N يوم"""
        now = datetime.now(timezone.utc)
        حد = now + timedelta(days=أيام)

        شهادات = Certificate.query.filter(
            Certificate.معرف_المستأجر == معرف_المستأجر,
            Certificate.الحالة == CertificateStatus.نشطة.value,
            Certificate.تاريخ_الانتهاء <= حد,
            Certificate.تاريخ_الانتهاء > now,
        ).order_by(Certificate.تاريخ_الانتهاء).all()

        return [{
            **ش.to_dict(),
            "أيام_متبقية": (ش.تاريخ_الانتهاء - now).days
        } for ش in شهادات]

    # ──────────────────────────────────────────────
    # وظائف مساعدة خاصة
    # ──────────────────────────────────────────────
    @staticmethod
    def _توليد_مفتاح_عام(خوارزمية: PQCAlgorithm) -> str:
        """محاكاة توليد مفتاح عام (في الإنتاج: استخدم liboqs)"""
        حجم = خوارزمية.حجم_المفتاح_عام or 800
        return secrets.token_hex(min(حجم, 1000))

    @staticmethod
    def _حساب_بصمة(مفتاح: str, اسم: str, مستأجر: str) -> str:
        """حساب بصمة SHA-512 للشهادة"""
        محتوى = f"{مفتاح}:{اسم}:{مستأجر}:{secrets.token_hex(8)}"
        return hashlib.sha512(محتوى.encode()).hexdigest()

    @staticmethod
    def _توليد_X509(اسم_شائع: str, منظمة: str,
                     دولة: str, خوارزمية: str) -> str:
        """توليد بنية X.509 تقريبية بالمعطيات"""
        return (
            f"-----BEGIN CERTIFICATE-----\n"
            f"CN={اسم_شائع}, O={منظمة}, C={دولة}\n"
            f"Algorithm: {خوارزمية}\n"
            f"Serial: {uuid.uuid4().hex.upper()}\n"
            f"-----END CERTIFICATE-----"
        )
